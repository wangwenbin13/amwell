package com.amwell.vo;

/**
 * 线路排班vo
 * @author Administrator
 * 
 */
public class LineClassVo{

	private String orderDate;//排班日期

	private String orderStartTime;//发车时间
	
	private String vehicleNumber;//车牌号
	
	private int lineSubType;//线路子类型 0:上下班 1:旅游
	
	private String lineSubTypeS;//类型文字
	
	private String lineName;//线路名称
	
	private int lineTime;//预计行程时间(分)
	
	private String lineTimeStr;
	
	private String remark;//备注

	private String lineBaseId;//线路信息ID

	public String getOrderDate() {
		return orderDate;
	}

	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}

	public String getOrderStartTime() {
		return orderStartTime;
	}

	public void setOrderStartTime(String orderStartTime) {
		this.orderStartTime = orderStartTime;
	}

	public String getVehicleNumber() {
		return vehicleNumber;
	}

	public void setVehicleNumber(String vehicleNumber) {
		this.vehicleNumber = vehicleNumber;
	}

	public int getLineSubType() {
		return lineSubType;
	}

	public void setLineSubType(int lineSubType) {
		this.lineSubType = lineSubType;
	}

	public String getLineName() {
		return lineName;
	}

	public void setLineName(String lineName) {
		this.lineName = lineName;
	}

	public int getLineTime() {
		return lineTime;
	}

	public void setLineTime(int lineTime) {
		this.lineTime = lineTime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getLineBaseId() {
		return lineBaseId;
	}

	public void setLineBaseId(String lineBaseId) {
		this.lineBaseId = lineBaseId;
	}

	public String getLineSubTypeS() {
		return lineSubTypeS;
	}

	public void setLineSubTypeS(String lineSubTypeS) {
		this.lineSubTypeS = lineSubTypeS;
	}

	public String getLineTimeStr() {
		return lineTimeStr;
	}

	public void setLineTimeStr(String lineTimeStr) {
		this.lineTimeStr = lineTimeStr;
	}
}
