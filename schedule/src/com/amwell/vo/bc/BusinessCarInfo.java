package com.amwell.vo.bc;

import com.amwell.entity.base.BaseEntity;

/**
 * 包车车辆信息
 * @author 胡双
 *
 */
public class BusinessCarInfo extends BaseEntity{
	
	private static final long serialVersionUID = 1376413271624908056L;

	// 主键
	private String info_id; 
	
	// 竞价ID
	private String bidding_id;
	
	//车辆类型
	private String carType;
	
	//车辆座位数
	private int carSeats;
	
	//车辆年龄
	private int carAge;
	
	//车辆数
	private int totalCars;
	
	//创建人，报价人的商户ID
	private String createBy;
	
	//创建时间
	private String createOn;
	
	//列表排序
	private int orderNum;

	public String getInfo_id() {
		return info_id;
	}

	public void setInfo_id(String infoId) {
		info_id = infoId;
	}

	public String getBidding_id() {
		return bidding_id;
	}

	public void setBidding_id(String biddingId) {
		bidding_id = biddingId;
	}

	public String getCarType() {
		return carType;
	}

	public void setCarType(String carType) {
		this.carType = carType;
	}

	public int getCarSeats() {
		return carSeats;
	}

	public void setCarSeats(int carSeats) {
		this.carSeats = carSeats;
	}

	public int getCarAge() {
		return carAge;
	}

	public void setCarAge(int carAge) {
		this.carAge = carAge;
	}

	public int getTotalCars() {
		return totalCars;
	}

	public void setTotalCars(int totalCars) {
		this.totalCars = totalCars;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateOn() {
		return createOn;
	}

	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}

	public int getOrderNum() {
		return orderNum;
	}

	public void setOrderNum(int orderNum) {
		this.orderNum = orderNum;
	}
	
}
