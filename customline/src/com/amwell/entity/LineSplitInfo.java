package com.amwell.entity;

/**
 * 拆分线路
 * 
 * @author shawn.zheng
 * 
 */
public class LineSplitInfo {

	/**
	 * 主键
	 */
	private String sid;

	/**
	 * 起始站点
	 */
	private String bstation;

	/**
	 * 结束站点
	 */
	private String estation;

	/**
	 * 所属线路Id
	 */
	private String linebaseid;

	/**
	 * 是否有效 0-无效 1-有效
	 */
	private String iswork;

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getBstation() {
		return bstation;
	}

	public void setBstation(String bstation) {
		this.bstation = bstation;
	}

	public String getEstation() {
		return estation;
	}

	public void setEstation(String estation) {
		this.estation = estation;
	}

	public String getLinebaseid() {
		return linebaseid;
	}

	public void setLinebaseid(String linebaseid) {
		this.linebaseid = linebaseid;
	}

	public String getIswork() {
		return iswork;
	}

	public void setIswork(String iswork) {
		this.iswork = iswork;
	}

}
