package com.amwell.entity;

import java.math.BigDecimal;

/**
 * 充值明细Vo
 * @author wangwenbin
 *
 * 2014-8-20
 */
public class RechargeVo {

	
	//---------------- stat_passenger_recharge -----
	/**
	 * 充值时间
	 */
	private String retime;
	
	/**
	 * 流水号
	 */
	private String rerunNo;
	
	/**
	 * 充值额度 (金额)
	 */
	private BigDecimal moneyLimit;
	
	/**
	 * 充值方式
	 * 0:无              1:余额支付        2:财付通          3:银联            4:支付宝
	 */
	private Integer reModel;
	private String reModel_s;
	
	
	//--------------- passenger_info ----------
	/**
	 * 乘客昵称
	 */
	private String nickName;
	
	/**
	 * 乘客回显ID
	 */
	private String displayId;

	public String getRetime() {
		return retime;
	}

	public void setRetime(String retime) {
		this.retime = retime;
	}

	public String getRerunNo() {
		return rerunNo;
	}

	public void setRerunNo(String rerunNo) {
		this.rerunNo = rerunNo;
	}

	public BigDecimal getMoneyLimit() {
		return moneyLimit;
	}

	public void setMoneyLimit(BigDecimal moneyLimit) {
		this.moneyLimit = moneyLimit;
	}

	public Integer getReModel() {
		return reModel;
	}

	public void setReModel(Integer reModel) {
		this.reModel = reModel;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getDisplayId() {
		return displayId;
	}

	public void setDisplayId(String displayId) {
		this.displayId = displayId;
	}

	public String getReModel_s() {
		return reModel_s;
	}

	public void setReModel_s(String reModelS) {
		reModel_s = reModelS;
	}
	
	
}
