package com.amwell.entity;

public class Excel {
	private String excel_id;
	private String excel_name;
	private String excel_path;
	private String templete_code;
	private String add_time;
	private String start_row;
	private String start_column;
	public String getStart_row() {
		return start_row;
	}
	public void setStart_row(String startRow) {
		start_row = startRow;
	}
	public String getStart_column() {
		return start_column;
	}
	public void setStart_column(String startColumn) {
		start_column = startColumn;
	}
	public String getExcel_id() {
		return excel_id;
	}
	public void setExcel_id(String excelId) {
		excel_id = excelId;
	}
	public String getExcel_name() {
		return excel_name;
	}
	public void setExcel_name(String excelName) {
		excel_name = excelName;
	}
	public String getExcel_path() {
		return excel_path;
	}
	public void setExcel_path(String excelPath) {
		excel_path = excelPath;
	}
	public String getTemplete_code() {
		return templete_code;
	}
	public void setTemplete_code(String templeteCode) {
		templete_code = templeteCode;
	}
	public String getAdd_time() {
		return add_time;
	}
	public void setAdd_time(String addTime) {
		add_time = addTime;
	}
	
}
