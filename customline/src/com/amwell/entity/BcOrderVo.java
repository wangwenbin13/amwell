package com.amwell.entity;

import java.io.Serializable;
import java.math.BigDecimal;
/**
 * 
 * @author wangwenbin
 *
 * 2014-11-26
 */
/**
 * 包车订单Vo
 */
public class BcOrderVo implements Serializable{

	/**
	 * 主健ID
	 */
	private String orderId;
	
	/**
	 * 订单号
	 */
	private String orderNo;
	
	/**
	 * 支付时间(下单时间)
	 */
	private String payTime;
	
	/**
	 * 用户昵称
	 */
	private String nickName;
	
	/**
	 * 回显ID
	 */
	private String displayId;
	
	/**
	 * 联系电话
	 */
	private String telephone;
	
	/**
	 * 包车类型
	 * 1:活动包车 2:商务接送 3:过港接送
	 */
	private String businessType;
	
	/**
	 * 包车方式
	 * 1：单趟 2:往返 3:包天
	 */
	private String charteredMode;
	
	/**
	 * 出发时间
	 */
	private String startTime;
	
	/**
	 * 返程时间
	 */
	private String returnTime;
	
	/**
	 * 包车商家
	 */
	private String brefName;
	
	/**
	 * 金额
	 */
	private BigDecimal totalPrice;
	
	/**
	 * 支付状态
	 * 1:待调度 2：已调度(同时输入司机、车辆) 3：已完成 4:已退票
	 */
	private String orderStatus;
	
	/**
	 * 支付方式
	 * 1:支付宝 2：银联
	 */
	private String payModel;
	
	/**
	 * 用车联系人
	 */
	private String contacts;
	
	/**
	 * 用车联系人电话
	 */
	private String contactPhone;
	
	/**
	 * 起点
	 */
	private String beginAddress;
	
	/**
	 * 终点
	 */
	private String endAddress;
	
	/**
	 * 总人数
	 * @return
	 */
	private Integer totalPassengers;
	
	/**
	 * 总车辆数
	 * @return
	 */
	private Integer totalCars;
	
	/**
	 * 车龄 
	 * 车龄 0:不限 1:1年以内 2:3年以内 3:5年以内
	 * @return
	 */
	private String carAge;
	
	/**
	 * 是否需要发票
	 * 0:不需要发票 1:需要发票
	 * @return
	 */
	private Integer needInvoice;
	
	/**
	 * 发票抬头
	 * @return
	 */
	private String invoiceHeader;
	
	/**
	 * 行程备注
	 * @return
	 */
	private String journeyRemark;
	
	/**
	 * 备注
	 * @return
	 */
	private String remark;
	
	

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getPayTime() {
		return payTime;
	}

	public void setPayTime(String payTime) {
		this.payTime = payTime;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getBusinessType() {
		return businessType;
	}

	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public String getCharteredMode() {
		return charteredMode;
	}

	public void setCharteredMode(String charteredMode) {
		this.charteredMode = charteredMode;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getReturnTime() {
		return returnTime;
	}

	public void setReturnTime(String returnTime) {
		this.returnTime = returnTime;
	}

	public String getBrefName() {
		return brefName;
	}

	public void setBrefName(String brefName) {
		this.brefName = brefName;
	}

	public BigDecimal getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(BigDecimal totalPrice) {
		this.totalPrice = totalPrice;
	}

	public String getOrderStatus() {
		return orderStatus;
	}

	public void setOrderStatus(String orderStatus) {
		this.orderStatus = orderStatus;
	}

	public String getPayModel() {
		return payModel;
	}

	public void setPayModel(String payModel) {
		this.payModel = payModel;
	}

	public String getContacts() {
		return contacts;
	}

	public void setContacts(String contacts) {
		this.contacts = contacts;
	}

	public String getContactPhone() {
		return contactPhone;
	}

	public void setContactPhone(String contactPhone) {
		this.contactPhone = contactPhone;
	}

	public String getBeginAddress() {
		return beginAddress;
	}

	public void setBeginAddress(String beginAddress) {
		this.beginAddress = beginAddress;
	}

	public String getEndAddress() {
		return endAddress;
	}

	public void setEndAddress(String endAddress) {
		this.endAddress = endAddress;
	}

	public Integer getTotalPassengers() {
		return totalPassengers;
	}

	public void setTotalPassengers(Integer totalPassengers) {
		this.totalPassengers = totalPassengers;
	}

	public Integer getTotalCars() {
		return totalCars;
	}

	public void setTotalCars(Integer totalCars) {
		this.totalCars = totalCars;
	}

	public String getCarAge() {
		return carAge;
	}

	public void setCarAge(String carAge) {
		this.carAge = carAge;
	}

	public Integer getNeedInvoice() {
		return needInvoice;
	}

	public void setNeedInvoice(Integer needInvoice) {
		this.needInvoice = needInvoice;
	}

	public String getInvoiceHeader() {
		return invoiceHeader;
	}

	public void setInvoiceHeader(String invoiceHeader) {
		this.invoiceHeader = invoiceHeader;
	}

	public String getJourneyRemark() {
		return journeyRemark;
	}

	public void setJourneyRemark(String journeyRemark) {
		this.journeyRemark = journeyRemark;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getDisplayId() {
		return displayId;
	}

	public void setDisplayId(String displayId) {
		this.displayId = displayId;
	}

	
}
