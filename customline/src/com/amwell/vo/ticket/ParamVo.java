package com.amwell.vo.ticket;

import java.io.Serializable;

/**
 * 
 * @author wangwebin
 *
 */
/***
 * 参数实体类
 */
public class ParamVo implements Serializable{

	private static final long serialVersionUID = 5124421174525780634L;

	/**线路ID**/
	private String lineBaseId;
	
	/**线路名称**/
	private String lineName;
	
	/**新班次ID**/
	private String newClassId;
	
	/**乘客ID**/
	private String passengerId;
	
	/**商家ID**/
	private String businessId;
	
	/**新班次ID --改签前**/
	private String oldClassId;
	
	/**旧线路ID**/
	private String oldLineBaseId;
	
	/**新线路ID**/
	private String newLineBaseId;

	public String getLineBaseId() {
		return lineBaseId;
	}

	public void setLineBaseId(String lineBaseId) {
		this.lineBaseId = lineBaseId;
	}

	public String getLineName() {
		return lineName;
	}

	public void setLineName(String lineName) {
		this.lineName = lineName;
	}

	public String getNewClassId() {
		return newClassId;
	}

	public void setNewClassId(String newClassId) {
		this.newClassId = newClassId;
	}

	public String getPassengerId() {
		return passengerId;
	}

	public void setPassengerId(String passengerId) {
		this.passengerId = passengerId;
	}

	public String getBusinessId() {
		return businessId;
	}

	public void setBusinessId(String businessId) {
		this.businessId = businessId;
	}

	public String getOldClassId() {
		return oldClassId;
	}

	public void setOldClassId(String oldClassId) {
		this.oldClassId = oldClassId;
	}

	public String getOldLineBaseId() {
		return oldLineBaseId;
	}

	public void setOldLineBaseId(String oldLineBaseId) {
		this.oldLineBaseId = oldLineBaseId;
	}

	public String getNewLineBaseId() {
		return newLineBaseId;
	}

	public void setNewLineBaseId(String newLineBaseId) {
		this.newLineBaseId = newLineBaseId;
	}
	
	
}
