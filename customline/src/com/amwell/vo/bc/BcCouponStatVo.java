package com.amwell.vo.bc;

import java.io.Serializable;

/**
 * 
 * @author wangwenbin
 *
 * 2015-3-13
 */
/**
 * 包车优惠券Vo
 */
public class BcCouponStatVo implements Serializable{

	/**用户帐号**/
	private String telephone;
	
	/**订单号**/
	private String orderId;
	
	/**优惠券使用时间**/
	private String userTime;
	
	/**优惠券领取时间**/
	private String getTime;
	
	/**优惠券名称**/
	private String couponName;
	
	/**优惠券面值**/
	private String giftValue;
	
	/**优惠券上线时间**/
	private String upLineTime;
	
	/**优惠券创建时间**/
	private String createOn;
	
	/**优惠金额**/
	private String giftMoney;
	
	/**总价**/
	private String price;
	
	/**飞单价格**/
	private String giftLeftMoney;
	
	/**实际支付总价**/
	private String totalPrice;

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getOrderId() {
		return orderId;
	}

	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}

	public String getUserTime() {
		return userTime;
	}

	public void setUserTime(String userTime) {
		this.userTime = userTime;
	}

	public String getGetTime() {
		return getTime;
	}

	public void setGetTime(String getTime) {
		this.getTime = getTime;
	}

	public String getCouponName() {
		return couponName;
	}

	public void setCouponName(String couponName) {
		this.couponName = couponName;
	}

	public String getGiftValue() {
		return giftValue;
	}

	public void setGiftValue(String giftValue) {
		this.giftValue = giftValue;
	}

	public String getUpLineTime() {
		return upLineTime;
	}

	public void setUpLineTime(String upLineTime) {
		this.upLineTime = upLineTime;
	}

	public String getCreateOn() {
		return createOn;
	}

	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}

	public String getGiftMoney() {
		return giftMoney;
	}

	public void setGiftMoney(String giftMoney) {
		this.giftMoney = giftMoney;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getGiftLeftMoney() {
		return giftLeftMoney;
	}

	public void setGiftLeftMoney(String giftLeftMoney) {
		this.giftLeftMoney = giftLeftMoney;
	}

	public String getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(String totalPrice) {
		this.totalPrice = totalPrice;
	}
	
	
}
