package com.amwell.vo;

public class MessageLogVo {
	
	private String localId;//主键
	
	private String userId;  //用户ID
	
	private String msgId;  //乘客表ID
	
	private int readFlag;  //软件消息:已读标志 0：未读 1：已读 --->短信消息 ：只表示发送对应关系
	
	private String sendPhoneNo;  //发送的手机号码 
	
	private String sendTime;  //  发送的时间
	
	
	public String getLocalId() {
		return localId;
	}

	public void setLocalId(String localId) {
		this.localId = localId;
	}
	
	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getMsgId() {
		return msgId;
	}

	public void setMsgId(String msgId) {
		this.msgId = msgId;
	}

	public int getReadFlag() {
		return readFlag;
	}

	public void setReadFlag(int readFlag) {
		this.readFlag = readFlag;
	}

	public String getSendPhoneNo() {
		return sendPhoneNo;
	}

	public void setSendPhoneNo(String sendPhoneNo) {
		this.sendPhoneNo = sendPhoneNo;
	}

	public String getSendTime() {
		return sendTime;
	}

	public void setSendTime(String sendTime) {
		this.sendTime = sendTime;
	}


	

}
