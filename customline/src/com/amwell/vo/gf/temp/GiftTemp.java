package com.amwell.vo.gf.temp;


import com.amwell.entity.base.BaseEntity;

/**
 * 礼品实体类
 * @author huxiaojun
 *
 */
public class GiftTemp extends BaseEntity {
	private String giftPriId;//varchar(35) NOT NULL主键ID
	private String giftId;//int(10) NOT NULL礼品ID
	private String giftName;//varchar(32) NOT NULL礼品名称
	private String giftType;//tinyint(4) NOT NULL礼类类型:1:通用型礼品（班车、包车、拼车通用）2:上下班班车礼品 3:包车礼品 4:拼车礼品 5.推荐有奖礼品
	private String giftValue;//int(10) NOT NULL礼品面值
	private String giftCon;//int(10) NOT NULL约束条件
	private String state;//tinyint(2) NULL使用状态 0:未使用 1:使用中 2:已删除
	private String operatorId;//varchar(35) NOT NULL操作人ID
	private String operateTime;//varchar(19) NOT NULL操作时间
	private String parentGiftId;//int(10) NULL父礼品ID（用于推荐有奖发礼品包）
	public String getGiftPriId() {
		return giftPriId;
	}
	public void setGiftPriId(String giftPriId) {
		this.giftPriId = giftPriId;
	}
	public String getGiftId() {
		return giftId;
	}
	public void setGiftId(String giftId) {
		this.giftId = giftId;
	}
	public String getGiftName() {
		return giftName;
	}
	public void setGiftName(String giftName) {
		this.giftName = giftName;
	}
	public String getGiftType() {
		return giftType;
	}
	public void setGiftType(String giftType) {
		this.giftType = giftType;
	}
	public String getGiftValue() {
		return giftValue;
	}
	public void setGiftValue(String giftValue) {
		this.giftValue = giftValue;
	}
	public String getGiftCon() {
		return giftCon;
	}
	public void setGiftCon(String giftCon) {
		this.giftCon = giftCon;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getOperatorId() {
		return operatorId;
	}
	public void setOperatorId(String operatorId) {
		this.operatorId = operatorId;
	}
	public String getOperateTime() {
		return operateTime;
	}
	public void setOperateTime(String operateTime) {
		this.operateTime = operateTime;
	}
	public String getParentGiftId() {
		return parentGiftId;
	}
	public void setParentGiftId(String parentGiftId) {
		this.parentGiftId = parentGiftId;
	}
}
