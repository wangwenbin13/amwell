package com.amwell.vo;

public class CompanyLine {
	private String companyLineId;
	private String companyId;
	private String lineBaseId;

	public String getCompanyLineId() {
		return companyLineId;
	}

	public void setCompanyLineId(String companyLineId) {
		this.companyLineId = companyLineId;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getLineBaseId() {
		return lineBaseId;
	}

	public void setLineBaseId(String lineBaseId) {
		this.lineBaseId = lineBaseId;
	}

}
