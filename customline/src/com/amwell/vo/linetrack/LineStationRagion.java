package com.amwell.vo.linetrack;

import com.amwell.vo.StationInfo;

/**
 * 站点区间
 * 
 * @author shawn.zheng
 * 
 */
public class LineStationRagion {
	/**
	 * 线路Id
	 */
	private String lineBaseId;

	/**
	 * 站点Id
	 */
	private String stationInfoId;

	/**
	 * 起始站点
	 */
	private StationInfo startStation;

	/**
	 * 结束站点
	 */
	private StationInfo endStation;

	/**
	 * 是否设置了区间中间点
	 */
	private String isSetRagion;

	/**
	 * 中间点数量
	 */
	private Integer totalRagionNum;

	public StationInfo getStartStation() {
		return startStation;
	}

	public void setStartStation(StationInfo startStation) {
		this.startStation = startStation;
	}

	public StationInfo getEndStation() {
		return endStation;
	}

	public void setEndStation(StationInfo endStation) {
		this.endStation = endStation;
	}

	public String getLineBaseId() {
		return lineBaseId;
	}

	public void setLineBaseId(String lineBaseId) {
		this.lineBaseId = lineBaseId;
	}

	public String getStationInfoId() {
		return stationInfoId;
	}

	public void setStationInfoId(String stationInfoId) {
		this.stationInfoId = stationInfoId;
	}

	public String getIsSetRagion() {
		return isSetRagion;
	}

	public void setIsSetRagion(String isSetRagion) {
		this.isSetRagion = isSetRagion;
	}

	public Integer getTotalRagionNum() {
		return totalRagionNum;
	}

	public void setTotalRagionNum(Integer totalRagionNum) {
		this.totalRagionNum = totalRagionNum;
	}

}
