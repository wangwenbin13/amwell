package com.amwell.vo.app;

/**
 *线路_用户申请专线
 */
@SuppressWarnings("all")
public class LineUserApplication implements java.io.Serializable {

	private String applicationId;       //主键
	private String applicationTime;		//申请时间
	private String passengerId;			//乘客ID
	private String lineType;				//申请路线类型 0:上下班 1：自由行
	private String startAddress;		//申请路线起点
	private String endAddress;			//申请路线终点
	private String startTime;			//申请发车时间
	private String remark;				//备注信息


	public String getApplicationId() {
		return this.applicationId;
	}

	public void setApplicationId(String applicationId) {
		this.applicationId = applicationId;
	}

	public String getApplicationTime() {
		return this.applicationTime;
	}

	public void setApplicationTime(String applicationTime) {
		this.applicationTime = applicationTime;
	}

	public String getPassengerId() {
		return this.passengerId;
	}

	public void setPassengerId(String passengerId) {
		this.passengerId = passengerId;
	}

	public String getLineType() {
		return this.lineType;
	}

	public void setLineType(String lineType) {
		this.lineType = lineType;
	}

	public String getStartAddress() {
		return this.startAddress;
	}

	public void setStartAddress(String startAddress) {
		this.startAddress = startAddress;
	}

	public String getEndAddress() {
		return this.endAddress;
	}

	public void setEndAddress(String endAddress) {
		this.endAddress = endAddress;
	}

	public String getStartTime() {
		return this.startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getRemark() {
		return this.remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

}