package com.pig84.ab.vo;


/**
 * 乘客信息（redis）
 */
public class User implements java.io.Serializable {
	private String passengerId;			//乘客ID
	private String telephone;			//手机号码   
	private String caishenghuoremark;   //彩生活备用字段
	private String openId;				//微信用户openid
	private String sessionId;			//sessionId
	private String headerPicUrl;		//乘客头像URL
	
	
	
	public String getHeaderPicUrl() {
		return headerPicUrl;
	}
	public void setHeaderPicUrl(String headerPicUrl) {
		this.headerPicUrl = headerPicUrl;
	}
	public String getSessionId() {
		return sessionId;
	}
	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}
	public String getPassengerId() {
		return passengerId;
	}
	public void setPassengerId(String passengerId) {
		this.passengerId = passengerId;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getCaishenghuoremark() {
		return caishenghuoremark;
	}
	public void setCaishenghuoremark(String caishenghuoremark) {
		this.caishenghuoremark = caishenghuoremark;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	
}
