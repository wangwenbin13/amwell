package com.pig84.ab.vo;

import java.io.Serializable;

/**
 * 
 * @author wangwenbin
 *
 */
/**
 * 退票申请实体
 */
public class ApplyReturn implements Serializable{

	private static final long serialVersionUID = 1L;

	/**申请退票ID**/
	private String applyReturnId;
	
	/**线路_乘客班次表 的localIds**/
	private String localIds;
	
	/**班次ID**/
	private String lineClassIds;
	
	/**订单号**/
	private String leaseOrderNo;
	
	/**原退票的总价**/
	private String oldReturn;
	
	/**手续费百分比**/
	private String percent;
	
	/**实际退票金额**/
	private String realReturn;
	
	/**手续费**/
	private String realPoundage;
	
	/**0:为退票  1:已退票**/
	private String type;
	
	/**乘客Id**/
	private String passengerId;
	
	/**乘客手机号码**/
	private String telephone;
	
	/**申请时间**/
	private String applyTime;
	
	/**退票日期**/
	private String returnDates;
	
	/**支付方式 0：无 1：余额支付，2：财付通 3：银联 4：支付宝 5:微信 6:(APP)微信**/
	private String payModel;

	public String getApplyReturnId() {
		return applyReturnId;
	}

	public void setApplyReturnId(String applyReturnId) {
		this.applyReturnId = applyReturnId;
	}

	public String getLocalIds() {
		return localIds;
	}

	public void setLocalIds(String localIds) {
		this.localIds = localIds;
	}

	public String getLineClassIds() {
		return lineClassIds;
	}

	public void setLineClassIds(String lineClassIds) {
		this.lineClassIds = lineClassIds;
	}

	public String getLeaseOrderNo() {
		return leaseOrderNo;
	}

	public void setLeaseOrderNo(String leaseOrderNo) {
		this.leaseOrderNo = leaseOrderNo;
	}

	public String getOldReturn() {
		return oldReturn;
	}

	public void setOldReturn(String oldReturn) {
		this.oldReturn = oldReturn;
	}

	public String getPercent() {
		return percent;
	}

	public void setPercent(String percent) {
		this.percent = percent;
	}

	public String getRealReturn() {
		return realReturn;
	}

	public void setRealReturn(String realReturn) {
		this.realReturn = realReturn;
	}

	public String getRealPoundage() {
		return realPoundage;
	}

	public void setRealPoundage(String realPoundage) {
		this.realPoundage = realPoundage;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPassengerId() {
		return passengerId;
	}

	public void setPassengerId(String passengerId) {
		this.passengerId = passengerId;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getApplyTime() {
		return applyTime;
	}

	public void setApplyTime(String applyTime) {
		this.applyTime = applyTime;
	}

	public String getReturnDates() {
		return returnDates;
	}

	public void setReturnDates(String returnDates) {
		this.returnDates = returnDates;
	}

	public String getPayModel() {
		return payModel;
	}

	public void setPayModel(String payModel) {
		this.payModel = payModel;
	}
	
	
}
