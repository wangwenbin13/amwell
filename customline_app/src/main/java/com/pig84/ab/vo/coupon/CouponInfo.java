package com.pig84.ab.vo.coupon;


/**
 * 优惠券信息
 * @author gxt
 *
 */
public class CouponInfo {
	private String id;//int(11) NOT NULL主键ID
	private String couponName;//varchar(32) NOT NULL优惠券名称
	private String couponValue;//decimal(4,2) NOT NULL优惠券面额
	private String couponCon;//decimal(4,2) NOT NULL优惠券门槛
	private String effectiveTime;//varchar(19) NULL使用的有效开始时间(预留)
	private String expirationTime;//varchar(19) NULL使用的有效结束时间(预留)
	private String delayDays;//int(10) NULL后延天数（单位：天）
	private String isDel;//tinyint(1) NULL使用状态 0:正常 1:已删除
	private String couponGroupId;//int(11) NULL组合券的批次号
	private String couponGroupName;//varchar(35) NULL组合券名称
	private String num;//int(10) NULL组合券内该优惠券的个数
	private String couponCount;//int(10) NULL优惠券发行总数
	private String createBy;//varchar(35) NOT NULL创建人
	private String userName;//用户名
	private String createOn;//varchar(19) NOT NULL创建时间
	
	private String grantId;//临时变量，存储发放id
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCouponName() {
		return couponName;
	}
	public void setCouponName(String couponName) {
		this.couponName = couponName;
	}
	public String getCouponValue() {
		return couponValue;
	}
	public void setCouponValue(String couponValue) {
		this.couponValue = couponValue;
	}
	public String getCouponCon() {
		return couponCon;
	}
	public void setCouponCon(String couponCon) {
		this.couponCon = couponCon;
	}
	public String getEffectiveTime() {
		return effectiveTime;
	}
	public void setEffectiveTime(String effectiveTime) {
		this.effectiveTime = effectiveTime;
	}
	public String getExpirationTime() {
		return expirationTime;
	}
	public void setExpirationTime(String expirationTime) {
		this.expirationTime = expirationTime;
	}
	public String getDelayDays() {
		return delayDays;
	}
	public void setDelayDays(String delayDays) {
		this.delayDays = delayDays;
	}
	public String getIsDel() {
		return isDel;
	}
	public void setIsDel(String isDel) {
		this.isDel = isDel;
	}
	public String getCouponGroupId() {
		return couponGroupId;
	}
	public void setCouponGroupId(String couponGroupId) {
		this.couponGroupId = couponGroupId;
	}
	public String getCouponGroupName() {
		return couponGroupName;
	}
	public void setCouponGroupName(String couponGroupName) {
		this.couponGroupName = couponGroupName;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getCreateOn() {
		return createOn;
	}
	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}
	public String getGrantId() {
		return grantId;
	}
	public void setGrantId(String grantId) {
		this.grantId = grantId;
	}
	public String getCouponCount() {
		return couponCount;
	}
	public void setCouponCount(String couponCount) {
		this.couponCount = couponCount;
	}
}
