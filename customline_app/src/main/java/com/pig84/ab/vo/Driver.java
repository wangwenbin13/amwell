package com.pig84.ab.vo;


/**
 * 司机信息（redis）
 */
public class Driver implements java.io.Serializable {
	private String driverId;//司机ID
	private String telephone;//司机电话
	private String businessId;//商户ID
	private String headerPicUrl;//varchar(256) NULL司机头像URL
	private String sessionid;//sessionid
	private String driverName;//司机姓名
	
	
	
	public String getDriverName() {
		return driverName;
	}
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}
	public String getSessionid() {
		return sessionid;
	}
	public void setSessionid(String sessionid) {
		this.sessionid = sessionid;
	}
	public String getDriverId() {
		return driverId;
	}
	public void setDriverId(String driverId) {
		this.driverId = driverId;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getBusinessId() {
		return businessId;
	}
	public void setBusinessId(String businessId) {
		this.businessId = businessId;
	}
	public String getHeaderPicUrl() {
		return headerPicUrl;
	}
	public void setHeaderPicUrl(String headerPicUrl) {
		this.headerPicUrl = headerPicUrl;
	}
}
