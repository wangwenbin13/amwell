package com.pig84.ab.vo;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * 
 * @author wangwenbin
 *
 */
/**
 * 改签Vo
 */
public class ChangeTicketVo implements Serializable{
	private static final long serialVersionUID = 8978474667714526994L;
	
	/**(原)班次ID**/
	private String lineClassId;
	
	/**乘客ID**/
	private String passengerId;
	
	/**(临时存放的新)班次ID**/
	private String oldclassid;
	
	/**乘车日期**/
	private String orderDate;
	
	/**发车时间**/
	private String orderStartTime;
	
	/**线路ID**/
	private String lineBaseId;
	
	/**商家ID**/
	private String businessId;
	
	/**司机ID**/
	private String driverId;
	
	/**车辆ID**/
	private String vehicleId;
	
	/**状态（0:正常  1:改签中   2:已改签）**/
	private Integer status;
	
	/**线路名称**/
	private String lineName;
	
	/**票价**/
	private BigDecimal price;
	
	/**优惠ID**/
	private String couponGiftId;

	public String getLineClassId() {
		return lineClassId;
	}

	public void setLineClassId(String lineClassId) {
		this.lineClassId = lineClassId;
	}

	public String getPassengerId() {
		return passengerId;
	}

	public void setPassengerId(String passengerId) {
		this.passengerId = passengerId;
	}

	public String getOldclassid() {
		return oldclassid;
	}

	public void setOldclassid(String oldclassid) {
		this.oldclassid = oldclassid;
	}

	public String getOrderDate() {
		return orderDate;
	}

	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}

	public String getOrderStartTime() {
		return orderStartTime;
	}

	public void setOrderStartTime(String orderStartTime) {
		this.orderStartTime = orderStartTime;
	}

	public String getLineBaseId() {
		return lineBaseId;
	}

	public void setLineBaseId(String lineBaseId) {
		this.lineBaseId = lineBaseId;
	}

	public String getBusinessId() {
		return businessId;
	}

	public void setBusinessId(String businessId) {
		this.businessId = businessId;
	}

	public String getDriverId() {
		return driverId;
	}

	public void setDriverId(String driverId) {
		this.driverId = driverId;
	}

	public String getVehicleId() {
		return vehicleId;
	}

	public void setVehicleId(String vehicleId) {
		this.vehicleId = vehicleId;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getLineName() {
		return lineName;
	}

	public void setLineName(String lineName) {
		this.lineName = lineName;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public String getCouponGiftId() {
		return couponGiftId;
	}

	public void setCouponGiftId(String couponGiftId) {
		this.couponGiftId = couponGiftId;
	}
	
	
}
