package com.pig84.ab.vo;

/**
 * 推荐有奖明细表
 * @author gongxueting
 *
 */
public class RecommendAwardInfo {
	private String id;//varchar(32) NOT NULL
	private String oldUserPhone;//varchar(16) NOT NULL老用户手机号码
	private String newUserPhone;//varchar(16) NOT NULL新用户手机号码
	private String oldUserGiftId;//varchar(35) NOT NULL老用户优惠券ID
	private String newUserGiftId;//varchar(35) NOT NULL拉新用户优惠券ID
	private String createOn;//varchar(64) NULL时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOldUserPhone() {
		return oldUserPhone;
	}
	public void setOldUserPhone(String oldUserPhone) {
		this.oldUserPhone = oldUserPhone;
	}
	public String getNewUserPhone() {
		return newUserPhone;
	}
	public void setNewUserPhone(String newUserPhone) {
		this.newUserPhone = newUserPhone;
	}
	public String getOldUserGiftId() {
		return oldUserGiftId;
	}
	public void setOldUserGiftId(String oldUserGiftId) {
		this.oldUserGiftId = oldUserGiftId;
	}
	public String getNewUserGiftId() {
		return newUserGiftId;
	}
	public void setNewUserGiftId(String newUserGiftId) {
		this.newUserGiftId = newUserGiftId;
	}
	public String getCreateOn() {
		return createOn;
	}
	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}
}
