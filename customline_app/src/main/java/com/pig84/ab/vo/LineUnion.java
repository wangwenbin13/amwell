package com.pig84.ab.vo;

import java.io.Serializable;

/**
 * 
 * @author wangwenbin
 *
 */
/**
 * 线路联合查询实体
 */
public class LineUnion implements Serializable{

	private static final long serialVersionUID = 1L;

	/**类型  1:发布线路   2:招募线路**/
	private String type;
	
	/**起点**/
	private String bstation;
	
	/**终点**/
	private String estation;
	
	/**主线路ID**/
	private String linebaseid;
	
	/**拆分线路或者招募线路的id**/
	private String id;
	
	/**发车时间**/
	private String startTime;
	
	/**行程时间**/
	private String costTime;
	
	/**线路长度**/
	private String lineKm;
	
	/**线路价格**/
	private String linePrice;
	
	/**招募报名人数**/
	private Integer applyNum;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getBstation() {
		return bstation;
	}

	public void setBstation(String bstation) {
		this.bstation = bstation;
	}

	public String getEstation() {
		return estation;
	}

	public void setEstation(String estation) {
		this.estation = estation;
	}

	public String getLinebaseid() {
		return linebaseid;
	}

	public void setLinebaseid(String linebaseid) {
		this.linebaseid = linebaseid;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getCostTime() {
		return costTime;
	}

	public void setCostTime(String costTime) {
		this.costTime = costTime;
	}

	public String getLineKm() {
		return lineKm;
	}

	public void setLineKm(String lineKm) {
		this.lineKm = lineKm;
	}

	public String getLinePrice() {
		return linePrice;
	}

	public void setLinePrice(String linePrice) {
		this.linePrice = linePrice;
	}

	public Integer getApplyNum() {
		return applyNum;
	}

	public void setApplyNum(Integer applyNum) {
		this.applyNum = applyNum;
	}
	
	
}

