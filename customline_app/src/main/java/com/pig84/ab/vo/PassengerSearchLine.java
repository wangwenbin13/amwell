package com.pig84.ab.vo;

import java.io.Serializable;

/**
 * 
 * @author wangwenbin
 *
 */
/**
 * 用户线路搜索实体
 */
public class PassengerSearchLine implements Serializable {

	private static final long serialVersionUID = 1L;

	/** 搜索线路记录ID **/
	private String searchLineId;

	/** 起点名称 **/
	private String startName;

	/** 起点经度 **/
	private String sloni;

	/** 起点纬度 **/
	private String slati;

	/** 终点名字 **/
	private String endName;

	/** 终点经度 **/
	private String eloni;

	/** 终点纬度 **/
	private String elati;

	/** 搜索时间 **/
	private String searchTime;

	public PassengerSearchLine(String startName, String sloni, String slati, String endName,
			String eloni, String elati, String searchTime) {
		this.startName = startName;
		this.sloni = sloni;
		this.slati = slati;
		this.endName = endName;
		this.eloni = eloni;
		this.elati = elati;
		this.searchTime = searchTime;
	}
	

	public PassengerSearchLine() {
	}



	public String getSearchLineId() {
		return searchLineId;
	}

	public void setSearchLineId(String searchLineId) {
		this.searchLineId = searchLineId;
	}

	public String getStartName() {
		return startName;
	}

	public void setStartName(String startName) {
		this.startName = startName;
	}

	public String getSloni() {
		return sloni;
	}

	public void setSloni(String sloni) {
		this.sloni = sloni;
	}

	public String getSlati() {
		return slati;
	}

	public void setSlati(String slati) {
		this.slati = slati;
	}

	public String getEndName() {
		return endName;
	}

	public void setEndName(String endName) {
		this.endName = endName;
	}

	public String getEloni() {
		return eloni;
	}

	public void setEloni(String eloni) {
		this.eloni = eloni;
	}

	public String getElati() {
		return elati;
	}

	public void setElati(String elati) {
		this.elati = elati;
	}

	public String getSearchTime() {
		return searchTime;
	}

	public void setSearchTime(String searchTime) {
		this.searchTime = searchTime;
	}

}
