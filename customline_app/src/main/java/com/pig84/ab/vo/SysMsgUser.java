package com.pig84.ab.vo;

/**
 *线路_用户申请专线
 */
@SuppressWarnings("all")
public class SysMsgUser implements java.io.Serializable {

	private String localId;
	private String userId;
	private String msgId;
	private String readFlag;
	private String sendPhoneNo;
	private String sendTime;
	
	
	public String getLocalId() {
		return localId;
	}
	public void setLocalId(String localId) {
		this.localId = localId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getMsgId() {
		return msgId;
	}
	public void setMsgId(String msgId) {
		this.msgId = msgId;
	}
	public String getReadFlag() {
		return readFlag;
	}
	public void setReadFlag(String readFlag) {
		this.readFlag = readFlag;
	}
	public String getSendPhoneNo() {
		return sendPhoneNo;
	}
	public void setSendPhoneNo(String sendPhoneNo) {
		this.sendPhoneNo = sendPhoneNo;
	}
	public String getSendTime() {
		return sendTime;
	}
	public void setSendTime(String sendTime) {
		this.sendTime = sendTime;
	}
}
