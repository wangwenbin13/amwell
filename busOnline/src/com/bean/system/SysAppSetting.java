package com.bean.system;

/**
 * 系统_app相关设置
 * 
 * @author shawn.zheng
 * 
 */
public class SysAppSetting {
	/**
	 * 设置Id
	 */
	private String settingId;

	/**
	 * 订单有效期
	 */
	private Integer orderValiteTime;

	/**
	 * 最多购票
	 */
	private Integer maxTickets;

	/**
	 * 按此预定，最多预定天数
	 */
	private Integer maxDays;

	/**
	 * 包月预定，最多预定月数
	 */
	private Integer maxMonths;

	/**
	 * 创建人
	 */
	private String createBy;

	/**
	 * 创建时间
	 */
	private String createOn;

	/**
	 * 修改人
	 */
	private String updateBy;

	/**
	 * 修改时间
	 */
	private String updateOn;

	/**
	 * 微信自动回复内容
	 */
	public String WXreply;

	public String getSettingId() {
		return settingId;
	}

	public void setSettingId(String settingId) {
		this.settingId = settingId;
	}

	public Integer getOrderValiteTime() {
		return orderValiteTime;
	}

	public void setOrderValiteTime(Integer orderValiteTime) {
		this.orderValiteTime = orderValiteTime;
	}

	public Integer getMaxTickets() {
		return maxTickets;
	}

	public void setMaxTickets(Integer maxTickets) {
		this.maxTickets = maxTickets;
	}

	public Integer getMaxDays() {
		return maxDays;
	}

	public void setMaxDays(Integer maxDays) {
		this.maxDays = maxDays;
	}

	public Integer getMaxMonths() {
		return maxMonths;
	}

	public void setMaxMonths(Integer maxMonths) {
		this.maxMonths = maxMonths;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateOn() {
		return createOn;
	}

	public void setCreateOn(String createOn) {
		this.createOn = createOn;
	}

	public String getUpdateBy() {
		return updateBy;
	}

	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}

	public String getUpdateOn() {
		return updateOn;
	}

	public void setUpdateOn(String updateOn) {
		this.updateOn = updateOn;
	}

	public String getWXreply() {
		return WXreply;
	}

	public void setWXreply(String wXreply) {
		WXreply = wXreply;
	}

}
